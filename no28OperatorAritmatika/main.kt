	operand2 = 20
	hasilOperasi = operand1 + operand2
	println("Operator Aritmatika + (Penjumlahan) :\nHasil Operasi, $operand1 + $operand2 = $hasilOperasi\n")

	operand1 = 10
	operand2 = 20
	hasilOperasi = operand1 - operand2
	println("Operasi Aritmatika - (Pengurangan):\nHasil Operasi, $operand1 - $operand2 = $hasilOperasi\n")

	operand1 = 20
	operand2 = 20
	hasilOperasi = operand1 * operand2
	println("Operasi Aritmatika * (Perkalian):\nHasil Operasi, $operand1 * $operand2 = $hasilOperasi\n")

	operand1 = 4
	operand2 = 2
	hasilOperasi = operand1 / operand2
	println("Operasi Aritmatika / (Pembagian):\nHasil Operasi, $operand1 / $operand2 = $hasilOperasi\n")

	operand1 = 5
	operand2 = 2
	hasilOperasi = operand1 % operand2
}