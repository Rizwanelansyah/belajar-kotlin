fun main () {
  var pegawai = arrayOf("Ujang Rudini", "Fajar", "Tira", "Bagus")

  println("--------------------------------------------")
  println("Latihan Iterasi Melalui Indeksnya (Range) : Rizwan Elansyah")
  println("--------------------------------------------")

  for (index in pegawai.indices) {
    println(pegawai[index])
  }

}