fun main() {

    val fruits = arrayOf<String>("Apple", "Mango", "Banana", "Orange", "Apple")
    val distinct = fruits.distinct()

    println("================================================================")
    println("Latihan Mengambil Nilai Berbeda Pada Array : Rizwan Elansyah")
    println("================================================================")

    for (item in distinct) {
        println(item)
    }

}