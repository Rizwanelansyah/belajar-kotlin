fun main() {

    val fruits = arrayOf<String>("Kelapa", "Rambutan")
    val nums = arrayOf<Int>(2, 3, 4)
    val logics = arrayOf<Boolean>(true, (2 > 1), false)
    val genders = arrayOf<Char>('L', 'P')

    println("==============================================")
    println("Latihan Mendeclarasikan Array : Rizwan Elansyah")
    println("==============================================")

    println("Array String : ")
    print(fruits[0])
    print("")
    print(fruits[1])
    println()

    println("\nArray Int : ")
    print(nums[0])
    print(" ")
    print(nums[1])
    print(" ")
    print(nums[2])
    println()

    println("\nArray Boolean : ")
    print(logics[0])
    print(" ")
    print(logics[1])
    print(" ")
    print(logics[2])
    println()

    println("\nArray Char : ")
    print(genders[0])
    print(" ")
    print(genders[1])
    println()

}
