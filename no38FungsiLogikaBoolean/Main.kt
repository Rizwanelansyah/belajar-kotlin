fun main() {
    val x:Boolean = true
    val y:Boolean = false
    val z:Boolean = true

    println("==============================================================================================")
    println("Latihan Fungsi Operator Boolean: Rizwan Elansyah")
    println("==============================================================================================")

    println("x.and(y) = " + x.and(y))
    println("x.or(y) = " + x.or(y))
    println("x.xor(y) = " + x.xor(y))
    println("x.and(z) = " + x.and(z))
}